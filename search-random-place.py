def weighted_choices(values: List[Tuple[int, str]], k: int = 1) -> Union[List[str], str]:
    wcs = choices(list(v[1] for v in values), weights=list(log(v[0]) for v in values), k=k)
    return wcs[0] if k == 1 else wcs


