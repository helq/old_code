def comp(b: int, e: int) -> int:
    if e == 2:
        return (b ** e) % (10**10)  # type: ignore
    else:
        if e % 2 == 0:
            return comp((b**2) % (10**10), e//2)
        else:
            return (comp(b, e-1) * b) % (10**10)


